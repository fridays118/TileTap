function draw_lightWorld()
	for i,row in ipairs(lw) do
		for j,tile in ipairs(row) do
			if tilemap[j][i] ~= 1 then
			if tile == 1 then
		colorStart(0,0,0,30)
			love.graphics.rectangle("fill",j*tileSize,i*tileSize,tileSize,tileSize)
			colorEnd()
				end
				if tile == 2 then
		colorStart(0,0,0,40)
			love.graphics.rectangle("fill",j*tileSize,i*tileSize,tileSize,tileSize)
			colorEnd()
				end
				if tile == 3 then
		colorStart(0,0,0,50)
			love.graphics.rectangle("fill",j*tileSize,i*tileSize,tileSize,tileSize)
			colorEnd()
				end
			end
		end
	end
end